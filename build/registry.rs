//! Emit `{OUT_DIR}/edge_groups.rs` which defines `pub const EDGE_GROUPS`.
//!
//! Must stay in lockstep with `build/groups.rs::PASS_GROUPS` and the
//! `menaiStrait` 13th entry.

use crate::build::groups::{MENAI_NAME, PASS_GROUPS};
use std::io::Write;
use std::path::Path;

pub fn write_edge_groups_rs(out_dir: &Path) -> Result<(), String> {
    let path = out_dir.join("edge_groups.rs");
    let mut s = String::new();
    s.push_str("// AUTOGENERATED by build.rs (ENG-4678). DO NOT EDIT.\n");
    s.push_str("// To change the registry, edit build/groups.rs PASS_GROUPS\n");
    s.push_str("// and rebuild.\n\n");
    s.push_str("/// Names of the 13 baked-in edge groups, in stable order.\n");
    s.push_str("///\n");
    s.push_str("/// Indices 0..12 match `Graph::groups[i]` in every archive.\n");
    s.push_str("pub const EDGE_GROUPS: &[&str; 13] = &[\n");
    for (_tag, public) in PASS_GROUPS {
        s.push_str(&format!("    \"{public}\",\n"));
    }
    s.push_str(&format!("    \"{MENAI_NAME}\",\n"));
    s.push_str("];\n");

    let mut f =
        std::fs::File::create(&path).map_err(|e| format!("create {}: {e}", path.display()))?;
    f.write_all(s.as_bytes())
        .map_err(|e| format!("write {}: {e}", path.display()))?;
    Ok(())
}
